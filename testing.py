# testing

# we can potentially have lots errors in our code - so we should test. 
# write tests is cheaper solution than refactoring our code after deployment.
# types of tests:
# unit tests - tests each block of code/function in isolation.
# integration testing - how does the code work in relation to other code.
# user-acceptance - by customers/staff - do the app do what its supposed to do. 
# qa testing - rules + regulations + compliance
# non - functional testing (monitoring):
# performance, scalability, usablity, up time.  
# maintenence testing - we push a change to our code and see what happens. 

